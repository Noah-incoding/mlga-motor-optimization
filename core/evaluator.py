def calculate_true_score(
    eff: float,
    Tstp: float,
    Tmscore: float,
    Ilimite: float,
    pf: float,
) -> float:
    """
    Reconstruct final score using the same MATLAB-style rule.
    Lower is better because final score = -Ans.
    """
    vals = [eff, Tstp, Tmscore, Ilimite, pf]
    if any(pd.isna(x) for x in vals):
        return 0.0

    if Ilimite > 7 or pf >= 1 or pf < 0.75:
        return 0.0
    elif eff < 0.895:
        return -(eff / 0.895)
    else:
        effsc = eff / 0.905
        tstpsc = Tstp / 2.75
        tmsc = Tmscore / 5.25
        return -((effsc + tstpsc + tmsc) - 0.04)


