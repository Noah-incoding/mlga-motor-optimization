def cosd(x_deg: float) -> float:
    return float(np.cos(np.deg2rad(x_deg)))


def atand(x: float) -> float:
    return float(np.rad2deg(np.arctan(x)))


def safe_tansida(Im, Iw, X1, X2, V1):
    x = (Iw * (X1 + X2) / V1)
    inside = 1 - x**2
    if inside <= 0 or not np.isfinite(inside) or not np.isfinite(Iw):
        return 0.0
    tans = ((Im / Iw) + x) / np.sqrt(inside)
    if not np.isrealobj(tans) or not np.isfinite(tans):
        return 0.0
    return float(np.real(tans))


