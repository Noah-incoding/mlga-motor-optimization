class PhysicsEvaluator:
    DESIGN_COLS = ["bs0", "hs2", "steeth", "hr2", "rteeth", "turn"]

    def __init__(self, config: MachineConfig):
        self.config = config

    def build_input_dict(self, row: pd.Series | Dict[str, Any]) -> Dict[str, Any]:
        if isinstance(row, pd.Series):
            row = row.to_dict()

        v = asdict(self.config).copy()
        for col in self.DESIGN_COLS:
            v[col] = row[col]
        return v

    def _format_failed_result(
        self,
        v: Dict[str, Any],
        phase1: Dict[str, Any],
        phase2: Optional[Dict[str, Any]],
    ) -> Dict[str, Any]:
        reason = phase1.get("reason", "phase1_fail")
        if phase2 is not None and phase2.get("reason"):
            reason = phase2.get("reason")

        result = {
            # design vars
            "bs0": v["bs0"],
            "hs2": v["hs2"],
            "steeth": v["steeth"],
            "hr2": v["hr2"],
            "rteeth": v["rteeth"],
            "turn": v["turn"],

            # true targets
            "ok_true": 0,
            "reason_true": reason,
            "eff_true": np.nan,
            "Tstp_true": np.nan,
            "Tmscore_true": np.nan,
            "Ilimite_true": np.nan,
            "pf_true": np.nan,
            "Pin_true": np.nan,
            "Pout_true": np.nan,
            "loss_true": np.nan,

            # intermediate physics
            "R1": phase1.get("R1", np.nan),
            "R2": phase1.get("R2", np.nan),
            "s": phase1.get("s", np.nan),
            "X1": phase1.get("X1", np.nan),
            "X2": phase1.get("X2", np.nan),
            "Im": phase1.get("Im", np.nan),
            "Coreloss": phase1.get("Coreloss", np.nan),

            # optional I11 split
            "I11_real": np.nan,
            "I11_imag": np.nan,
            "I11_abs": np.nan,

            # final score / metadata
            "true_score": 0.0,
            "fitness_source": "physics",
            "selected_for_physics": True,
        }

        i11 = phase1.get("I11", np.nan + 1j*np.nan)
        if np.isfinite(np.real(i11)):
            result["I11_real"] = float(np.real(i11))
        if np.isfinite(np.imag(i11)):
            result["I11_imag"] = float(np.imag(i11))
        if np.isfinite(np.abs(i11)):
            result["I11_abs"] = float(np.abs(i11))

        if phase2 is not None:
            result["Pin_true"] = phase2.get("Pin", np.nan)
            result["Pout_true"] = phase2.get("Pout", np.nan)
            result["loss_true"] = phase2.get("loss", np.nan)
            result["pf_true"] = phase2.get("pf", np.nan)

        return result

    def _format_success_or_fail(
        self,
        v: Dict[str, Any],
        phase1: Dict[str, Any],
        phase2: Dict[str, Any],
    ) -> Dict[str, Any]:
        if not phase2.get("ok", False):
            return self._format_failed_result(v, phase1, phase2)

        eff = phase2.get("eff", np.nan)
        tstp = phase2.get("Tstp", np.nan)
        tmscore = phase2.get("Tmscore", np.nan)
        ilimite = phase2.get("Ilimite", np.nan)
        pf = phase2.get("pf", np.nan)
        true_score = calculate_true_score(eff, tstp, tmscore, ilimite, pf)

        i11 = phase1.get("I11", np.nan + 1j*np.nan)

        result = {
            # design vars
            "bs0": v["bs0"],
            "hs2": v["hs2"],
            "steeth": v["steeth"],
            "hr2": v["hr2"],
            "rteeth": v["rteeth"],
            "turn": v["turn"],

            # true targets
            "ok_true": 1,
            "reason_true": "ok",
            "eff_true": eff,
            "Tstp_true": tstp,
            "Tmscore_true": tmscore,
            "Ilimite_true": ilimite,
            "pf_true": pf,
            "Pin_true": phase2.get("Pin", np.nan),
            "Pout_true": phase2.get("Pout", np.nan),
            "loss_true": phase2.get("loss", np.nan),

            # intermediate physics
            "R1": phase1.get("R1", np.nan),
            "R2": phase1.get("R2", np.nan),
            "s": phase1.get("s", np.nan),
            "X1": phase1.get("X1", np.nan),
            "X2": phase1.get("X2", np.nan),
            "Im": phase1.get("Im", np.nan),
            "Coreloss": phase1.get("Coreloss", np.nan),

            # optional I11 split
            "I11_real": float(np.real(i11)) if np.isfinite(np.real(i11)) else np.nan,
            "I11_imag": float(np.imag(i11)) if np.isfinite(np.imag(i11)) else np.nan,
            "I11_abs": float(np.abs(i11)) if np.isfinite(np.abs(i11)) else np.nan,

            # final score / metadata
            "true_score": true_score,
            "fitness_source": "physics",
            "selected_for_physics": True,
        }
        return result

    def evaluate_one(self, row: pd.Series | Dict[str, Any]) -> Dict[str, Any]:
        v = self.build_input_dict(row)

        phase1 = calc_equiv_params_clean(v)
        if not phase1["ok"]:
            return self._format_failed_result(v, phase1, phase2=None)

        phase2 = solve_at_target_power_core(
            V1=v["V1"],
            I11=phase1["I11"],
            Im=phase1["Im"],
            R1=phase1["R1"],
            R2=phase1["R2"],
            X1=phase1["X1"],
            X2=phase1["X2"],
            s=phase1["s"],
            Coreloss=phase1["Coreloss"],
            f=v["f"],
            pole=v["pole"],
            Ptarget=self.config.Ptarget,
            step=self.config.step,
            max_iter=self.config.max_iter,
        )

        return self._format_success_or_fail(v, phase1, phase2)

    def evaluate_population(self, df: pd.DataFrame) -> pd.DataFrame:
        if df is None or len(df) == 0:
            return pd.DataFrame(columns=[
                "bs0", "hs2", "steeth", "hr2", "rteeth", "turn",
                "ok_true", "reason_true",
                "eff_true", "Tstp_true", "Tmscore_true", "Ilimite_true", "pf_true",
                "Pin_true", "Pout_true", "loss_true",
                "R1", "R2", "s", "X1", "X2", "Im", "Coreloss",
                "I11_real", "I11_imag", "I11_abs",
                "true_score", "fitness_source", "selected_for_physics",
            ])

        results = [self.evaluate_one(row) for _, row in df.iterrows()]
        return pd.DataFrame(results)

