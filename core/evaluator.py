def bh_H_from_B(B: float) -> float:
    """
    Interpolate H from B using CS350 B-H curve.

    Parameters
    ----------
    B : float
        Flux density (Tesla)

    Returns
    -------
    float
        Magnetic field intensity H
    """
    if not np.isfinite(B):
        return np.nan

    # B should normally be non-negative in this workflow
    if B < 0:
        B = abs(B)

    # clamp to curve range
    B_clamped = np.clip(B, CS350T[0], CS350T[-1])

    H = np.interp(B_clamped, CS350T, CS350H)
    return float(H)

